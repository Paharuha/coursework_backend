package coursework.model;

import lombok.AllArgsConstructor;
import lombok.Getter;
import lombok.NoArgsConstructor;
import lombok.Setter;

import javax.persistence.*;

@Entity
@Table(name = "subcontracting")
@Getter
@Setter
@NoArgsConstructor
@AllArgsConstructor
public class Subcontracting {
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Column(name = "id")
    private int id;

    @Column(name = "name")
        private String name;

    @Column(name = "value")
    private int value;
}
